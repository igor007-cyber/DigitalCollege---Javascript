let x = 4;

// Calcular a raiz quadrada (quadrado de x)
let raizQuadrada = x ** 2;
console.log(`${raizQuadrada} é o resultado da raiz quadrada`);

// Calcular ao cubo (x elevado a 3)
raizCubo = x ** 3;
console.log(`${raizCubo} é o resultado do cubo`);

// Calcular x elevado a um outro valor, neste caso 5
let y = 5;
let raiz = x ** y;
console.log(`${raiz} é o resultado de ${x} elevado a ${y}`);
